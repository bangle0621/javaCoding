package bronze5.Day4;

public class BOJ10171 {
	public static void main(String[] args) {
// 일반적으로 출력이 안되는 문자열이 혼재
// " 출력 위해서는 \" 로 사용, \출력 위해서는 \\
//		\    /\
//		 )  ( ')
//		(  /  )
//		 \(__)|
		System.out.println("\\    /\\");
		System.out.println(" )  ( ')");
		System.out.println("(  /  )");
		System.out.println(" \\(__)|");
		System.out.println("\"\"");
		
	}
}
